package io.github.namakurio.pemilos.model;

public class User {

    Integer id;
    String username;
    String name;
    String email;
    String api_token;

    public Integer getId() {
        return id;
    }

    public String getUsername() {
        return username;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getApi_token() {
        return api_token;
    }
}
